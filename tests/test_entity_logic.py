def test_get_hierarchy():
    from histarchexplorer.api.presentation_view import PresentationView
    pv = PresentationView(id=10, system_class='feature', view_class='v', title='T', description={}, aliases=[], start='S', end='E')
    place = Relation(id=1, name='P', system_class='place', geometries=[], geometry_json={})
    pv.relations = {'place': [place]}
    res = get_hierarchy(pv)
    assert len(res) == 1
    assert res[0].id == 1
    
    pv2 = PresentationView(id=20, system_class='stratigraphic_unit', view_class='v', title='T', description={}, aliases=[], start='S', end='E')
    feat = Relation(id=2, name='F', system_class='feature', relation_types=[{'relationTo': 20}])
    pv2.relations = {'feature': [feat], 'place': [place]}
    res = get_hierarchy(pv2)
    assert len(res) == 2

