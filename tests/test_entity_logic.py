def test_get_entity_images():
    from histarchexplorer import app
    files = [
        File(id=1, title='f1', render_type='image', main_image=True, license='L', public=True, url='U', mime_type='M', creator='C', license_holder='H', iiif_manifest='I', iiif_base_path='B', overlay=False, from_super_entity=False),
        File(id=2, title='f2', render_type='image', main_image=False, license='L', public=True, url='U', mime_type='M', creator='C', license_holder='H', iiif_manifest='I', iiif_base_path='B', overlay=False, from_super_entity=False),
        File(id=3, title='f3', render_type='unknown', main_image=False, license='L', public=True, url='U', mime_type='M', creator='C', license_holder='H', iiif_manifest='I', iiif_base_path='B', overlay=False, from_super_entity=False)
    ]
    with app.test_request_context():
        g.additional_files_for_overview = 1
        main, initial, all_imgs = get_entity_images(files)
        assert main.id == 1
        assert len(initial) == 1
        assert len(all_imgs) == 2

